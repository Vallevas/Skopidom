def set_paragraph_format(paragraph, space_after=Pt(12), line_spacing=1.5):
    """Настройка формата абзаца."""
    paragraph_format = paragraph.paragraph_format
    paragraph_format.space_after = space_after
    paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    paragraph_format.first_line_indent = Cm(1.25)  # Красная строка

def set_run_font(run, font_name='Times New Roman', size=Pt(14), bold=False, italic=False):
    """Настройка шрифта."""
    run.font.name = font_name
    run.font.size = size
    run.font.bold = bold
    run.font.italic = italic
    # Для корректного отображения кириллицы в Times New Roman
    run._element.rPr.rFonts.set(qn('w:eastAsia'), 'Times New Roman')

def add_heading(doc, text, level=1):
    """Добавление заголовка."""
    heading = doc.add_heading(text, level=level)
    heading.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in heading.runs:
        set_run_font(run, size=Pt(14) if level == 1 else Pt(14), bold=True)
    
    # Убираем красную строку у заголовков
    heading.paragraph_format.first_line_indent = Cm(0)
    heading.paragraph_format.space_after = Pt(12)
    return heading

def add_paragraph(doc, text, bold=False, italic=False, no_indent=False):
    """Добавление обычного абзаца."""
    p = doc.add_paragraph()
    set_paragraph_format(p, space_after=Pt(12))
    if no_indent:
        p.paragraph_format.first_line_indent = Cm(0)
    
    run = p.add_run(text)
    set_run_font(run, bold=bold, italic=italic)
    return p

def add_list_item(doc, text, level=0):
    """Добавление элемента списка."""
    p = doc.add_paragraph()
    set_paragraph_format(p, space_after=Pt(6))
    # Для списков делаем отступ, но не красную строку в классическом понимании
    p.paragraph_format.left_indent = Cm(1.25 + level * 0.5)
    p.paragraph_format.first_line_indent = Cm(-0.5) if level == 0 else Cm(0)
    
    bullet = "• " if level == 0 else "- "
    run = p.add_run(bullet + text)
    set_run_font(run)
    return p

